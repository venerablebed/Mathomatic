#import <Foundation/Foundation.h>
#import "Functions.h"


@implementation Functions

+ (int) add:(int)x to:(int)y {
    return x + y;
}


+ (int) subtract:(int)x from:(int)y {
    return y - x;
}


+ (int) multiply:(int)x with:(int)y {
    return x * y;
}


+ (float) divide:(float)x by:(float)y {
    return y / x;
}

@end
